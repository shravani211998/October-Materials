class DatatypesDemo {
	public static void main(String[] args) {
		byte a = 127;
		System.out.println("a = "+a);
		short b = 129;
		System.out.println("b = "+b);
		int c = 2147483647;
		System.out.println("c = "+c);
		long d = 2147483648L;
		System.out.println("d = "+d);
		float e = 3.14F;
		System.out.println("e = "+e);
		char f = 'A';
		System.out.println("f = "+f);

		String h = "Alexandar";
		System.out.println("h = "+h);
	}
}